#define MMIN(a, b) ((a) > (b) ? (b) : (a))
#define INF_MAX 0x3f3f3f3f

int shortestSubarray(int* nums, int numsSize, int k) {
    int res = INF_MAX;
    int sum = 0;

    for (int l = 0, r = 0; r < numsSize; ++r) {
        sum += nums[r];

        // 【为什么全为正数时代码正确？】：
        // 1. 单调递增：r 向右移动时，sum 一定会变大（或不变）。
        // 2. 单调递减：l 向右移动时，sum 一定会变小。
        //
        // 当 sum >= k 成立时，因为全是正数，当前窗口 [l, r] 就是以 r 结尾的
        // 候选解。此时为了找“最短”子数组，我们必须尝试把 l 向右移（缩小窗口）。
        // 只要 sum 依然 >= k，说明缩小的窗口仍然合法，继续缩，直到不满足为止。
        // 这种单调性保证了 while 循环能精确榨干当前 r 下的所有更短可能性。
        //
        // -----------------------------------------------------------------
        // 【为什么有负数时会错？反例模拟】：
        // 设 nums = [84, -37, 32, 40, 95], k = 167 (正确答案是后3项, 长度为3)
        //
        // 1. 右指针 r 走到末尾(r=4)时，把所有数都加了进来，此时 sum = 214。
        // 2. 满足 sum >= k (214 >= 167)，代码第一次进入下面的 while 循环：
        //    - 更新 res = MMIN(INF_MAX, 4 - 0 + 1) = 5
        //    - 减去左边界 nums[0](84)，sum 变成 214 - 84 = 130
        //    - 执行 ++l，此时 l 变成 1 (指向 -37)
        // 3. 准备进行下一次 while 循环条件检查：
        //    - 检查 sum >= k (130 >= 167)，发现不满足！
        //    - while 循环直接退出，外层 for 循环也结束了。
        //
        // 【致命问题】：
        // 此时 l 停在 1。如果 l 能继续往右移动到 2 (移出负数 -37)，
        // 那么 sum 会变成 130 - (-37) = 167，再次满足 >= k 的条件！
        // 这样就能把 res 更新为 4 - 2 + 1 = 3 (找到更短的正确答案)。
        //
        // 也就是说，由于负数 -37 的存在，导致 l 往右移本来可以让 sum 变大，
        // 但你的代码误以为 sum 减去 nums[0] 后已经太小了，提前退出了循环，
        // 导致左指针被卡死在前面，彻底漏掉了后面更短的优秀解。
        while (sum >= k) {
            res = MMIN(res, r - l + 1);
            sum -= nums[l];
            ++l;
        }
    }

    return res == INF_MAX ? -1 : res;
}
