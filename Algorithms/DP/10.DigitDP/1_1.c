// 计算 0 到 10^n - 1 之间，所有数位上数字都不重复的数字个数
int countNumbersWithUniqueDigits(int n) {
    if (n == 0) return 1;
    // 一位数（0~9）：总共有 10 个数字，全部满足各位数字唯一
    int res = 10;
    // 计算两位数、三位数 ... 直到 n 位数的符合条件的数字数量
    // i：当前计算的是几位数
    // cur：当前位数的符合条件的数字个数
    // left：当前位还剩下多少个数字可以选
    for (int i = 2, left = 9, cur = 9; i <= n; ++i) {
        // 两位数：9 * 9
        // 三位数：9 * 9 * 8
        // 四位数：9 * 9 * 8 * 7
        // 每多一位，可选数字就少一个
        cur *= left--;  // 计算当前位数的合法数字数量，left 自减 1
        res += cur;     // 累加到总答案中
    }
    return res;
}
